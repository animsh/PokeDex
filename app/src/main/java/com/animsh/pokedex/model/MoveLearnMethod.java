package com.animsh.pokedex.model;

public class MoveLearnMethod {
    private String name;
    private String url;

    public MoveLearnMethod(String name, String url) {
        this.name = name;
        this.url = url;
    }

    public String getName() {
        return name;
    }

    public String getUrl() {
        return url;
    }
}
